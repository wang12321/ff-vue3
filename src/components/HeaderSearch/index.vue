<template>
  <div :class="{'show':show}" class="header-search">
    <svg-icon
        style="height: 40px;line-height: 40px"
        name="search"
        font-size="17px"
        @click.stop="click"
    />
    <el-select
        ref="headerSearchSelect"
        v-model="search"
        :remote-method="querySearch"
        filterable
        default-first-option
        remote
        placeholder="Search"
        class="header-search-select"
        @change="change"
    >
      <el-option v-for="item in options" :key="item.path" :value="item" :label="item.item.title.join(' > ')" />
    </el-select>
  </div>
</template>

<script>
import Fuse from 'fuse.js'
import path from 'path'
import { computed, defineComponent, getCurrentInstance, ref, reactive, watch, toRefs, nextTick } from 'vue'
import { useStore } from 'vuex'
import { useRouter } from 'vue-router'

export default defineComponent({
  name: 'HeaderSearch',
  setup() {
    const store = useStore()
    const router = useRouter()
    const search = ref('')
    const options = ref([])
    const searchPool = ref([])
    const show = ref(false)
    const fuse = ref(undefined)
    const routes = computed(() => {
      return store.getters.permission_routes
    })
    const { ctx } = getCurrentInstance()

    const state = reactive({
      click: () => {
        show.value = !show.value
        if (show.value) {
          ctx.$refs.headerSearchSelect && ctx.$refs.headerSearchSelect.focus()
        }
      },
      close() {
        ctx.$refs.headerSearchSelect && ctx.$refs.headerSearchSelect.blur()
        options.value = []
        show.value = false
      },
      change(val) {
        router.push(val.item.path)
        search.value = ''
        options.value = []
        nextTick(() => {
          show.value = false
        })
      },
      initFuse(list) {
        fuse.value = new Fuse(list, {
          shouldSort: true,
          threshold: 0.4,
          location: 0,
          distance: 100,
          maxPatternLength: 32,
          minMatchCharLength: 1,
          keys: [{
            name: 'title',
            weight: 0.7
          }, {
            name: 'path',
            weight: 0.3
          }]
        })
      },
      // Filter out the routes that can be displayed in the sidebar
      // And generate the internationalized title
      generateRoutes(routes, basePath = '/', prefixTitle = []) {
        let res = []
        for (const router of routes) {
          // skip hidden router
          if (router.hidden) { continue }
          const data = {
            path: path.resolve(basePath, router.path),
            title: [...prefixTitle]
          }
          if (router.meta && router.meta.title) {
            data.title = [...data.title, router.meta.title]

            if (router.redirect !== 'noRedirect') {
              // only push the routes with title
              // special case: need to exclude parent router without redirect
              res.push(data)
            }
          }
          // recursive child routes
          if (router.children) {
            const tempRoutes = this.generateRoutes(router.children, data.path, data.title)
            if (tempRoutes.length >= 1) {
              res = [...res, ...tempRoutes]
            }
          }
        }
        return res
      },
      querySearch(query) {
        if (query !== '') {
          options.value = fuse.value.search(query)
        } else {
          options.value = []
        }
      }
    })
    watch(
      show,
      () => {
        searchPool.value = state.generateRoutes(routes.value)
        state.initFuse(searchPool.value)
      }
    )
    return {
      search,
      options,
      searchPool,
      show,
      fuse,
      routes,
      ...toRefs(state)
    }
  }

})

</script>

